/**
 * Possible Data Types in Secs/Gem.
 * Used here to allow fine tunning in custom tasks
 */
export enum SecsGemDataTypes {
    /** Ascii */
    A = "A",
    /** Binary */
    BI = "BI",
    /** Boolean */
    BO = "BO",
    /** Float (4 bytes) */
    F4 = "F4",
    /** Double (8 bytes) */
    F8 = "F8",
    /** Integer (1 byte) */
    I1 = "I1",
    /** Integer (2 bytes) */
    I2 = "I2",
    /** Integer (4 bytes) */
    I4 = "I4",
    /** Integer (8 bytes) */
    I8 = "I8",
    /** List */
    L = "L",
    /** Unsigned Int (1 byte) */
    U1 = "U1",
    /** Unsigned Int (2 bytes) */
    U2 = "U2",
    /** Unsigned Int (4 bytes) */
    U4 = "U4",
    /** Unsigned Int (8 bytes) */
    U8 = "U8"
}
